package Product;

public class ProductClass {
     private int PrdId;
     private String PrdName;
     private Float PrdCost;
     private int PrdQuantity;
     private String PrdExpiry;
     
     public int getPrdId() {
    	 return PrdId;
     }
     
     public void setPrdId(int PrdId) {
    	 this.PrdId = PrdId;
     }
     
     public String getPrdName() {
    	 return PrdName;
     }
     
     public void setPrdName(String PrdName) {
    	 this.PrdName = PrdName;
     }
     
     public Float getPrdCost() {
    	 return PrdCost;
     }
     
     public void setPrdCost(Float PrdCost) {
    	 this.PrdCost = PrdCost; 
     }
     public int getPrdQuantity() {
    	 return PrdQuantity;
     }
     
     public void setPrdQuantity(int PrdQuantity) {
    	 this.PrdQuantity = PrdQuantity; 
     }
     
     public String getPrdExpiry() {
    	 return PrdExpiry;
     }
     
     public void setPrdExpiry(String PrdExpiry) {
    	 this.PrdExpiry = PrdExpiry;
     }
     
    
}
